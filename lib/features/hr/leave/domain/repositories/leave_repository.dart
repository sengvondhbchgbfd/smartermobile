import '../entities/leave_entity.dart';

abstract class LeaveRepository {
  // ── Staff ──────────────────────────────────────────────────────────────────
  /// Submit a new leave request.
  //////////////////////////////////////////////////////////////////////////////
  Future<LeaveEntity> createLeave({
    required LeaveType leaveType,
    required DateTime startDate,
    required DateTime endDate,
    String? reason,
  });

  //////////////////////////////////////////////////////////////////////////////
  /// Get the current staff member's own leave requests.
  /// //////////////////////////////////////////////////////////////////////////
  Future<List<LeaveEntity>> getMyLeaves({int skip = 0, int limit = 50});
  //////////////////////////////////////////////////////////////////////////////
  /// Cancel one of the current staff member's leave requests.
  /// //////////////////////////////////////////////////////////////////////////
  Future<LeaveEntity> cancelLeave(int leaveId);

  // ── Manager ────────────────────────────────────────────────────────────────
  /// //////////////////////////////////////////////////////////////////////////
  /// List all leave requests (with optional filters).
  /// //////////////////////////////////////////////////////////////////////////
  ///
  Future<List<LeaveEntity>> getAllLeaves({
    int skip = 0,
    int limit = 50,
    LeaveStatus? status,
    LeaveType? leaveType,
  });

  /// //////////////////////////////////////////////////////////////////////////
  /// Get only pending leave requests.
  /// //////////////////////////////////////////////////////////////////////////

  Future<List<LeaveEntity>> getPendingLeaves({int skip = 0, int limit = 50});

  /// //////////////////////////////////////////////////////////////////////////
  /// Get leave statistics summary.
  /// //////////////////////////////////////////////////////////////////////////

  Future<Map<String, dynamic>> getLeaveSummary();

  /// //////////////////////////////////////////////////////////////////////////
  /// Get a single leave request by ID.
  /// //////////////////////////////////////////////////////////////////////////

  Future<LeaveEntity> getLeaveById(int leaveId);

  /// //////////////////////////////////////////////////////////////////////////
  /// Approve a leave request.
  /// //////////////////////////////////////////////////////////////////////////

  Future<LeaveEntity> approveLeave(int leaveId);

  /// //////////////////////////////////////////////////////////////////////////
  /// Reject a leave request.
  /// //////////////////////////////////////////////////////////////////////////

  Future<LeaveEntity> rejectLeave(int leaveId, {String? reason});

  /// //////////////////////////////////////////////////////////////////////////
  /// Delete a leave request.
  /// //////////////////////////////////////////////////////////////////////////

  Future<Map<String, dynamic>> deleteLeave(int leaveId);
}
